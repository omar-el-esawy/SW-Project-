package user;

public class CreditCard {

	double balance;
	String cardNumber;
	double getBalance()
	{
		return balance;
	}
	void setBalance(double balance)
	{
		this.balance=balance;
	}
	String getCardNumber()
	{
		return cardNumber;
	}
	void setCardNumber(String cardNumber)
	{
		this.cardNumber=cardNumber;
	}
	
}
